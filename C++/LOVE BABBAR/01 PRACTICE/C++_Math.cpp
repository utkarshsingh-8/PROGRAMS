#include <iostream>
#include <cmath>
using namespace std;

int main()
{
    // The max(x,y) function can be used to find the highest value of x and y:
    cout << max(5, 10) << endl;

    // the min(x,y) function can be used to find the lowest value of x and y:
    cout << min(5, 10) << endl;

    cout << sqrt(64) << endl;
    cout << round(2.6) << endl;
    cout << log(2) << endl;
    cout << abs(10) << endl;
    cout << acos(10) << endl;
    cout << asin(10) << endl;
    cout << cbrt(27) << endl;
    cout << ceil(27) << endl;
    cout << exp(27) << endl;
   

    return 0;
}